public class PrintSum {
    public static void main(String[] args) {

        /*
        print sum of all numbers from 1 to 10
         */
        int sum=0;
        for (int i=1;i<11;i++){

            // System.out.println(i);

             sum=sum+i;
        }
        System.out.println(sum);




    }//end of main
}//end of class
